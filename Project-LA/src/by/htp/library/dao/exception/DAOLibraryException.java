package by.htp.library.dao.exception;

public class DAOLibraryException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = -1805467150345332797L;

	public DAOLibraryException() {
		super();
	}

	public DAOLibraryException(String message, Throwable cause) {
		super(message, cause);
	}

	public DAOLibraryException(String message) {
		super(message);
	}

	public DAOLibraryException(Throwable cause) {
		super(cause);
	}

}
